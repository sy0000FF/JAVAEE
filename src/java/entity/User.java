package entity;

/**
 *
 * @author Drose
 */
public class User {

    private String id;
    private String sex;
    private String name;
    private String pass;
    private String email;

    public User() {
        id = "0";
        sex = "0";
        name = "0";
        pass = "0";
        email = "0";
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
